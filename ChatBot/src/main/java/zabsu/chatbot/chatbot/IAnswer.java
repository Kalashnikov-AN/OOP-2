package zabsu.chatbot.chatbot;

/**
 * Интерфейс IAnswer определяет структуру для различных типов ответов чат-бота.
 * Классы, реализующие этот интерфейс, должны уметь:
 * - определять, подходит ли входная строка под заданный шаблон;
 * - генерировать ответ на основе входной строки.
 */
public interface IAnswer {
    /**
     * Проверяет, соответствует ли входная строка определённому шаблону,
     * по которому данный тип ответа может сработать.
     *
     * @param s входная строка (обычно предварительно очищенная и нормализованная)
     * @return true, если шаблон сработал и данный тип ответа подходит; false — в противном случае
     */
    boolean is_matched(String s);

    /**
     * Генерирует ответ на основе входной строки.
     * Этот метод вызывается, если {@code is_matched} вернул true.
     *
     * @param s входная строка (запрос пользователя)
     * @return строка-ответ, сформированная данным типом ответа
     */
    String answer(String s);
}
